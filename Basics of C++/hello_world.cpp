#include <iostream>  // header-file preprocessor directive

using namespace std; // standard library

int main() // main function, int --> returns , void --> doesnt return
{
    // cout --> output stream object
    // endl --> end line
    // << --> stream insertion operator
    cout<<"Hello World!"<<endl;
    cout<<"hello \n"; // \n works like endl
    return 0; // ran fine without any errors
}